# program untuk menghitung volume silinder 
radius = 5   # mendefinisikan variabel radius dan memberikan nilai 5
tinggi = 10  # mendefinisikan variabel tinggi dan memberikan nilai 10
phi = 3.14   # mendefinisikan variabel phi dan memberikan nilai 2.14

# menghitung volume silinder dengan rumus pi * r^2 * tinggi
volume = phi * radius**2 * tinggi

# menampilkan hasil volume silinder
print("volume silinder:", volume)
